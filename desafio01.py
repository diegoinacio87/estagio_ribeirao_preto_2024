INDICE = 13
SOMA = 0
K = 0

while K < INDICE:
    K = K + 1
    SOMA = SOMA + K

print(SOMA)

# O valor final da variável SOMA será 91. #
# Explicação: #
# O loop while é executado 13 vezes, pois INDICE é igual a 13. #
# A cada iteração, K é incrementado em 1 e SOMA é incrementado em K. #
# No final do loop, K será igual a 14 e SOMA será a soma de todos os números de 1 a 13, que é 91. #